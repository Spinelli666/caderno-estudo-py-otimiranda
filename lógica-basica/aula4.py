'''
Tipos int e float
int: números inteiros
O tipo int representa qualquer número positivo ou negativo. int sem sinal é um número inteiro positivo.
'''
print(12)
print(12)
print(0)

'''
float: números decimais
O tipo float representa qualquer número positivo ou negativo com ponto flutuante
float sem sinal é considerado positivo
'''
print(12.0)
print(1.2)
print(1.2, -1.5)

'''
A função type mostra o tipo de dado
'''
print(type(12)) # Vai mostrar <class 'int'>
print(type("Be")) # Vai mostrar <class 'str'>